package training;

//수강 과정 자바빈
public class RegProcess {
	private int regNo;						//인덱스 번호
	private int stNo;						//훈련생 인덱스 번호
	private int prNo;						//과정 인덱스 번호
	
	public int getRegNo() {
		return regNo;
	}
	public void setRegNo(int regNo) {
		this.regNo = regNo;
	}
	public int getStNo() {
		return stNo;
	}
	public void setStNo(int stNo) {
		this.stNo = stNo;
	}
	public int getPrNo() {
		return prNo;
	}
	public void setPrNo(int prNo) {
		this.prNo = prNo;
	}
}
