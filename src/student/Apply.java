package student;

//잡트라넷 지원 자바빈
public class Apply {
	private int aNo;				//인덱스 번호
	private int stNo;				//훈련생 인덱스 번호
	private int jNo;				//잡트라넷 인덱스 번호
	private String aDate;			//지원 일자
	
	public int getaNo() {
		return aNo;
	}
	public void setaNo(int aNo) {
		this.aNo = aNo;
	}
	public int getStNo() {
		return stNo;
	}
	public void setStNo(int stNo) {
		this.stNo = stNo;
	}
	public int getjNo() {
		return jNo;
	}
	public void setjNo(int jNo) {
		this.jNo = jNo;
	}
	public String getaDate() {
		return aDate;
	}
	public void setaDate(String aDate) {
		this.aDate = aDate;
	}
}
